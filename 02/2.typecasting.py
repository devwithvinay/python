# TypeCasting :- The process of converting a variable from one data type to another - str() , int() , float() , bool()

name = "Vinay Kumar"
age = 23 
cgpa = 7.5
is_graduated = True

type(name)
print(type(name))
print(type(age))
print(type(cgpa))
print(type(is_graduated))

age = float(age)
print(age) # 23.0

cgpa = int(cgpa)
print(cgpa) # 7
