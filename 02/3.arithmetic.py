# Arithmetic  = + Addition
#               - Subraction
#               * Mutiplication
#               / Division (return a float)
#               // Integer Division 
#               % Remainder

#Assignment Operator :- += , -=


#Addition
friends = 5

friends += 7
print(friends)

# friends = friends + 1 
# we are NOT using --> "friends++" <-- it gives error


#Subtraction
pizza = 5

pizza -= 2
print(f"pizza is {pizza}")

#Mutiplication
balls = 3 

balls*=2
print(f"Nos of Balls is: {balls}")

# Division (return float)

Apples = 5

Apples /= 2
Apples = Apples / 2
print(f"Apples Divided by 2 is: {Apples}") # 2.5

# Integer Division 

Bottles = 7

Bottles //= 2
print(f"No.s of Bottles is: {Bottles}") # 3

# Remainders 

Watches = 5

Watches %= 3
print(f"remainder is :{Watches}") # 2





